    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String id_pelanggan = txtIDpelanggan.getText();
        String nama = txtNama.getText();
        String alamat = txtAlamat.getText();
        String no_telepon = txtTelepon.getText();

        if (id_pelanggan.trim().isEmpty() && nama.trim().isEmpty() && alamat.trim().isEmpty() && no_telepon.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan data pelanggan terlebih dahulu");

        } else if (id_pelanggan.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan ID Pelanggan dulu!");
        } else if (nama.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Nama Pelanggan dulu!");
        } else if (alamat.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Alamat terlebih dulu!");
        } else if (no_telepon.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Nomer Telepon terlebih dulu!");
        } else {
            PreparedStatement preparedStatement;
            try {
                String sql = "INSERT INTO pelanggan (id_pelanggan, nama, alamat, no_telepon) VALUES (?, ?, ?, ?)";
                conn = DriverManager.getConnection(koneksi, user, password);
                preparedStatement = conn.prepareStatement(sql);

                preparedStatement.setLong(1, Long.parseLong(id_pelanggan));
                preparedStatement.setString(2, nama);
                preparedStatement.setString(3, alamat);
                preparedStatement.setLong(4, Long.parseLong(no_telepon));

                preparedStatement.executeUpdate();
                JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal ditambahkan", "Error", JOptionPane.ERROR_MESSAGE);
            }
            tampil();
            bersih();
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String id_pelanggan = txtIDpelanggan.getText();
        String nama = txtNama.getText();
        String alamat = txtAlamat.getText();
        String no_telepon = txtTelepon.getText();

        if (id_pelanggan.trim().isEmpty() && nama.trim().isEmpty() && alamat.trim().isEmpty() && no_telepon.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "update data pelanggan terlebih dahulu");

        } else if (id_pelanggan.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan ID Pelanggan dulu!");
        } else if (nama.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Nama Pelanggan dulu yang ingin di update!");
        } else if (alamat.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Alamat terlebih dulu yang ingin di update!");
        } else if (no_telepon.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Nomer Telepon terlebih dulu yang ingin di update");
        } else {
            PreparedStatement preparedStatement = null;
            try {
                String sql = "UPDATE pelanggan set nama =?, alamat =?, no_telepon =? WHERE id_pelanggan = ?";
                conn = DriverManager.getConnection(koneksi, user, password);
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, txtNama.getText());
                ps.setString(2, txtAlamat.getText());
                ps.setInt(3, Integer.parseInt(txtTelepon.getText()));
                ps.setInt(4, Integer.parseInt(txtIDpelanggan.getText()));

                int row = ps.executeUpdate();
                if (row > 0) {
                    JOptionPane.showMessageDialog(this, "Update Berhasil");
                    tampil();
                    bersih();
                }

            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

