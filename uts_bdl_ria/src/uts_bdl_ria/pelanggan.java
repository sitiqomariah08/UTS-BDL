    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        DefaultTableModel ob = (DefaultTableModel) tbTabelPelanggan.getModel();
        TableRowSorter<DefaultTableModel> obj = new TableRowSorter<>(ob);
        tbTabelPelanggan.setRowSorter(obj);
        int selectedIndex = cbFilter.getSelectedIndex();
        switch (selectedIndex) {
            case 0:
                obj.setRowFilter(RowFilter.regexFilter(txtSearch.getText(), 0));
                break;
            case 1:
                obj.setRowFilter(RowFilter.regexFilter(txtSearch.getText(), 1));
                break;
            case 2:
                obj.setRowFilter(RowFilter.regexFilter(txtSearch.getText(), 2));
                break;
            case 3:
                obj.setRowFilter(RowFilter.regexFilter(txtSearch.getText(), 3));
                break;
            default:
                break;
        }

    }//GEN-LAST:event_txtSearchKeyReleased

