    public final void tampil() {
        tbmmhs = new DefaultTableModel(new String[]{"ID Pelanggan", "Nama Pelanggan", "Alamat", "No. Telepon"}, 0);
        String sql = "SELECT * FROM pelanggan ORDER BY id_pelanggan";

        try {
            Class.forName(driver);
            try (var conn = DriverManager.getConnection(koneksi, user, password); Statement stmt = conn.createStatement()) {
                ResultSet rs = stmt.executeQuery(sql);
                while (rs.next()) {
                    tbmmhs.addRow(new Object[]{
                        rs.getInt("id_pelanggan"),
                        rs.getString("nama").trim(),
                        rs.getString("alamat").trim(),
                        rs.getInt("no_telepon")
                    });
                    System.out.println(rs.getInt("id_pelanggan") + " " + rs.getString("nama") + " " + rs.getString("alamat") + " " + rs.getInt("no_telepon"));
                }
            }

        } catch (ClassNotFoundException | SQLException ex) {
//            Logger.getLogger(pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbTabelPelanggan.setModel(tbmmhs);
    }
